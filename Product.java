class Product {
	private String name;
	private int qty;
	private int price;
	
	public void setName(String name) {
		this.name=name;
	}
	
	public void setPrice(int price) {
		this.price=price;
	}
	
	public void setQty(int qty) {
		this.qty=qty;
	}
	
	public String getName() {
		return name;
	}
	
	public int getPrice() {
		return price;
	}
	
	public int getQty() {
		return qty;
	}
	

}